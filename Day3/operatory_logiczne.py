#operatory logiczne

print(True)
print(False)
print(type(True))

# operator porównania 2 znaki rowności
print(True == True)

# operator nierówności czy lewa strona jest różna od prawej strony
print(True != False)

# 1 and 2 - jeden i 2 ma być spelniony
print(True and True)
print(True and False)

# 1 or 2 - jeden bądz 2 ma być spełnion
print(True or True)
print(True or False)

# zaprzeczenie, odwrócenie wartości logicznej
print(not True)
print(not False)

